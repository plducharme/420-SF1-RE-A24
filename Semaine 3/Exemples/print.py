# Affichage d'une valeur
print(5)
grandeur = 186
print(grandeur)

# Affichage de plusieurs valeurs
print(5, 6, 7)
# En utilisant un séparateur
print(5, 6, 7, sep=';')
# En utilisant du remplacement de chaîne
print("Le nombre est : {}".format(5))

# En utilisant des f-strings
print(f"Léo est grand de {grandeur} cm")