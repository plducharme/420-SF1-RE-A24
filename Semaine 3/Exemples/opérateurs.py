print("=== Opérateurs ===")
nombre = 10
# addition
print(nombre + 5)
# multiplication
print(nombre * 5)
# division
print(nombre / 2)
# soustraction
print(nombre - 2)
# modulo
print(nombre % 2)
# puissance
print(nombre ** 2)
# division entière (plancher de la division)
print(nombre // 2)

# La priorité des opérateurs est similaire à celle des mathématiques. Les opérations entre parenthèses sont évaluées
# en premier, suivies des opérations de puissance, de multiplication et de division, et enfin des opérations
# d'addition et de soustraction. Les opérations de même priorité sont évaluées de gauche à droite.:
print("=== Priorité ===")
print(4 * 2 + 3)
print(4 * (2 + 3))

# Nombres
print("=== Nombres ===")
